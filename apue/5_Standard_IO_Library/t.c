#include <stdio.h>
int main(void)
{
		int a = 4, b = 3, c = 5;
		printf("sizeof(char): %lu\n", sizeof(char));
		printf("sizeof(int): %lu\n", sizeof(int));
		printf("sizeof(long): %lu\n", sizeof(long));
		printf("sizeof(char *): %lu\n", sizeof(char *));
		printf("(a, b): %d\n", (a, b, c));
		return(0);
}
